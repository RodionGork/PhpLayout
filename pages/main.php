<h2>PhpLayout</h2>

<h3>
Main page
</h3>

<p>
Welcome to PhpLayout. This is a small framework which can be used as a simple "layout" engine for your static web-site, or
as a full-stack MVC framework to build complex web-applications with support of user authentication, database work etc.
</p>

<p>
If you do not know how to start, try <b>PhpLayout-demo</b> version and read <a href="https://github.com/RodionGork/PhpLayout/wiki">project wiki</a>.
</p>
